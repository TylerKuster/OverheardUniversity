#import "OUTheme.h"

@implementation OUTheme

+ (UIColor*)brandColor
{
    return [UIColor colorWithRed:0.02f green:0.33f blue:0.45f alpha:1.0f];
}

+ (UIColor*)offWhiteColor
{
    return [UIColor colorWithRed:0.95f green:0.95f blue:0.95f alpha:1.0f];
}

@end
